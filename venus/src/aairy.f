c         ZSUM=1.E0-0.06944444E0/ZXI
c         AAIRY=0.2829497E0*EXP(-ZXI)*ZSUM*PX**-0.25E0
c      ELSE
cC     Ascending approximation "Abramowitz and Stegun, eq 10.4.2, page 446"
c         ZF=1.E0+0.1666666E0*PX**3 + 5.55555556E-3*PX**6
c         ZG=PX  +0.0833333E0*PX**4 + 1.98412698E-3*PX**7
c         AAIRY=0.3550280E0*ZF-0.2588194E0*ZG
c      ENDIF

      RETURN
      END

